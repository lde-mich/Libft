#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	count;
	int	logic;

	count = 0;
	logic = 1;
	i = 0;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' )
	{
		logic = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		count = count * 10;
		count = count + (str[i] - 48);
		i++;
	}
	return (count * logic);
}
